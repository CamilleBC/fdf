#include "projection.h"

void	set_idmat(double mat[3][3])
{
	int		i;
	int		j;

	i = -1;
	while (++i < 3)
	{
		j = -1;
		while (++j < 3)
		{
			mat[i][j] = (j == i) ? 1.00 : 0.00;
		}
	}
}
